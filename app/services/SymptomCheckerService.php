    // ============================================================
    // هل الرسالة تحتوي أعراضاً؟
    // ============================================================
    public function isSymptomMessage(string $message): bool
    {
        $msg = mb_strtolower($message, 'UTF-8');
        foreach (self::SYMPTOM_KEYWORDS as $kw) {
            if (mb_strpos($msg, $kw, 0, 'UTF-8') !== false) {
                return true;
            }
        }
        return false;
    }

    // ============================================================
    // محاولة Flask API
    // ============================================================
    private function classifyViaApi(string $text): ?array
    {
        $ctx = stream_context_create([
            'http' => [
                'method'        => 'POST',
                'header'        => "Content-Type: application/json\r\n",
                'content'       => json_encode(['text' => $text], JSON_UNESCAPED_UNICODE),
                'timeout'       => self::TIMEOUT,
                'ignore_errors' => true,
            ],
        ]);

        $raw = @file_get_contents(self::API_URL, false, $ctx);
        if ($raw === false) return null;

        $result = json_decode($raw, true);
        return (is_array($result) && ($result['success'] ?? false)) ? $result : null;
    }

    // ============================================================
    // واجهة عامة: أعد نتيجة التصنيف كامله (بدون بناء الرد)
    // ============================================================
    public function classify(string $message): ?array
    {
        // الـ local classifier أدق للهجة الفلسطينية
        $result = $this->classifyLocally($message);
        if ($result === null) {
            $apiResult = $this->classifyViaApi($message);
            if ($apiResult !== null && ($apiResult['confidence'] ?? 0) >= 60) {
                $result = $apiResult;
            }
        }
        if ($result === null) return null;
        $confidence = $result['confidence'] ?? 0;
        if ($confidence < 30 || empty($result['dept_name'])) return null;
        return $result;
    }

    // ============================================================
    // تصنيف محلي بالكلمات المفتاحية (بدون Flask)
    // ============================================================
    private function classifyLocally(string $text): ?array
    {
        $msg    = mb_strtolower($text, 'UTF-8');
        $best   = null;
        $bestScore = 0;

        foreach (self::LOCAL_MAP as $dept) {
            $score = 0;
            foreach ($dept['kw'] as $kw) {
                if (mb_strpos($msg, mb_strtolower($kw, 'UTF-8'), 0, 'UTF-8') !== false) {
                    $score++;
                }
            }
            if ($score > $bestScore) {
                $bestScore = $score;
                $best      = $dept;
            }
        }

        if ($best === null || $bestScore === 0) return null;

        return [
            'success'    => true,
            'dept_key'   => $best['key'],
            'dept_name'  => $best['name'],
            'confidence' => min(95, 55 + $bestScore * 12),
            'top3'       => [],
            'local'      => true,
        ];
    }

    // ============================================================
    // يبني رد الشات بوت — يجرّب API أولاً ثم المحلي
    // ============================================================
    public function buildReply(string $message): ?string
    {
        // الـ local classifier أدق للهجة الفلسطينية — شغّله أولاً
        $result = $this->classifyLocally($message);

        // استخدم Flask API فقط إذا المحلي ما لاقى شي
        if ($result === null) {
            $apiResult = $this->classifyViaApi($message);
            if ($apiResult !== null && ($apiResult['confidence'] ?? 0) >= 60) {
                $result = $apiResult;
            }
        }

        if ($result === null) return null;

        $deptKey    = $result['dept_key']   ?? '';
        $deptName   = $result['dept_name']  ?? '';
        $confidence = $result['confidence'] ?? 0;

        if ($confidence < 30 || $deptName === '') return null;

        // تغيير/إلغاء موعد
        if ($deptKey === 'تغيير_موعد') {
            return "يبدو أنك بدك تغير أو تلغي موعد موجود.\n\nاحكيلي: شو اسمك أو رقم الموعد؟ وبدك تغيير للوقت ولا إلغاء كلي؟";
        }

        $lines   = [];
        $lines[] = "بناءً على الأعراض اللي ذكرتها، أنصحك بمراجعة **{$deptName}**.";

        if (!empty($result['local'])) {
            $lines[] = "ننصحك بالكشف عند الدكتور لتأكيد التشخيص.";
        } elseif ($confidence >= 70) {
            $lines[] = "نسبة التطابق {$confidence}% — الأعراض واضحة.";
        }

        $top3 = $result['top3'] ?? [];
        if (!empty($result['local']) === false && count($top3) >= 2) {
            $second = $top3[1]['dept_name'] ?? '';
            if ($second && $second !== $deptName) {
                $lines[] = "قد يكون الأمر أيضاً مرتبطاً بـ **{$second}**.";
            }
        }

        $lines[] = '';
        $lines[] = "هل تريد أن أحجز لك موعداً في {$deptName}؟";

        return implode("\n", $lines);
    }
