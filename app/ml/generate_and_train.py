PREFIXES = [
    "أشعر بـ", "عندي", "أعاني من", "بحس بـ", "عندي مشكلة في",
    "من أمس عندي", "من يومين بعاني من", "من أسبوع عندي",
    "أنا مريض بـ", "بتعذب من", "ما أقدر أتحمل",
    "وجعني", "بعاني من", "صار عندي",
    "محتاج دكتور بسبب", "بدي أستشير دكتور عن",
    "", "", "", "", "",  # بدون بادئة
]

SUFFIXES = [
    "", "", "", "",  # بدون لاحقة
    " من أمس", " من يومين", " من أسبوع", " من شهر",
    " بشكل مزمن ومستمر", " بشكل متكرر",
    " وشديد جداً", " وخفيف نسبياً",
    " وما أعرف سببه", " وأنا قلقان عليه",
    " ومحتاج كشف عليه", " وبدي أشوف دكتور",
    " ولا أدوية تساعدني",
    " وأثر على حياتي اليومية",
]

CONNECTORS = [" و", " مع", "، بالإضافة إلى", " كمان عندي", " ومعه"]


def augment(symptom: str) -> str:
    """يولّد نسخة مُنوَّعة من العرض"""
    prefix = random.choice(PREFIXES)
    suffix = random.choice(SUFFIXES)
    return f"{prefix}{symptom}{suffix}".strip()


def combine_symptoms(symptoms: list, n: int = 2) -> str:
    """يدمج n من الأعراض في جملة واحدة"""
    chosen = random.sample(symptoms, min(n, len(symptoms)))
    result = chosen[0]
    for s in chosen[1:]:
        result += random.choice(CONNECTORS) + " " + s
    return result


def generate_dataset(target: int = 10500) -> tuple:
    """يولّد قاعدة بيانات التدريب"""
    texts, labels = [], []
    dept_keys = list(BASE_SYMPTOMS.keys())
    per_dept = target // len(dept_keys) + 10

    for key in dept_keys:
        symptoms = BASE_SYMPTOMS[key]
        dept = DEPT_BY_KEY[key]
        count = 0
        while count < per_dept:
            # نوع 1: عرض واحد مُنوَّع
            s = augment(random.choice(symptoms))
            texts.append(s)
            labels.append(key)
            count += 1

            # نوع 2: دمج عَرضين
            if len(symptoms) >= 2:
                s2 = combine_symptoms(symptoms, 2)
                texts.append(augment(s2))
                labels.append(key)
                count += 1

            # نوع 3: دمج ثلاثة أعراض
            if len(symptoms) >= 3 and count % 5 == 0:
                s3 = combine_symptoms(symptoms, 3)
                texts.append(augment(s3))
                labels.append(key)
                count += 1

    # خلط البيانات
    combined = list(zip(texts, labels))
    random.shuffle(combined)
    texts, labels = zip(*combined)
    return list(texts), list(labels)


